#include "hexagon_renderings.h"
#include <cassert>

// This file contains hexagon renderings at a variety of magnifications.
// For each, the diagonal segments are perfectly symmetric, and the hexagon is
// as close to regular as possible at the given resolution.
// Magnification 0 gives the smallest hexagons (3 px wide x 4 px high), allowing
// the most to appear in a given space.
// Magnification 4 gives the largest hexagons (59 px wide x 68 px high).  At
// publication quality resolution of 600 dpi, each hexagon will be 1/10" wide,
// so will be visually distinct.

HexagonRendering0 *hexagon_rendering0_ptr = NULL;
HexagonRendering1 *hexagon_rendering1_ptr = NULL;
HexagonRendering2 *hexagon_rendering2_ptr = NULL;
HexagonRendering3 *hexagon_rendering3_ptr = NULL;
HexagonRendering4 *hexagon_rendering4_ptr = NULL;

HexagonRendering &get_the_hexagon_rendering(int magnification_step, 
                                            int num_hues) {
  assert(magnification_step >= 0);
  switch (magnification_step) {
    case 0:
      return HexagonRendering0::get_the_hexagon_rendering0(num_hues);
      break;
    case 1:
      return HexagonRendering1::get_the_hexagon_rendering1(num_hues);
      break;
    case 2:
      return HexagonRendering2::get_the_hexagon_rendering2(num_hues);
      break;
    case 3:
      return HexagonRendering3::get_the_hexagon_rendering3(num_hues);
      break;
    case 4:
    default:
      return HexagonRendering4::get_the_hexagon_rendering4(num_hues);
      break;
  }
}

HexagonRendering0 &HexagonRendering0::get_the_hexagon_rendering0(int num_hues) {
  if (hexagon_rendering0_ptr == NULL) {
    hexagon_rendering0_ptr = new HexagonRendering0(num_hues);
  }
  return *hexagon_rendering0_ptr;
}

